x0 = [10; 1];
x1 = x0(1, 1);
x2 = x0(2, 1);
delta_f = [36 - 10 * x1 - 8 * x2; 36 - 10 * x2 - 8 * x1];
Hes = [-10 -8; -8 -10];
inhess = Hes^(-1);
d = -inhess * delta_f;
d1 = d(1, 1);
d2 = d(2, 1);
rho = (36 * d1 + 36 * d2 - 10 * d1 * x1 - 10 * d2 * x2 - 8 * d2 * x1 - 8 * d1 * x2) / (10 * d1.^2 + 10 * d2.^2 + 16 * d1 * d2);
newx = x0 + rho * d;
